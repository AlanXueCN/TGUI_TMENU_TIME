//----------------------判断字符串是否全角对齐函数-----------------
//未对齐时返回0
int Lan_IsCheckSBCCaseAlign(const char *pString,unsigned int Len)
{
  unsigned char i;
  for(i = 0; i < Len; i++){
    if(*(pString + i) >= 0x80) i++;
  }
  return (i == Len);
}
